def plot_corr(df,part,name):
    co_mtx = df.corr(numeric_only=True)
    sns.heatmap(co_mtx,cmap="coolwarm", annot=True,vmin=0 ,vmax=1, annot_kws={"size": 14})
    plt.title(f'{part} Correlation Matrix')
    plt.xticks(fontsize=14)
    plt.yticks(fontsize=14)
    plt.savefig(f'{name}.jpeg', format='jpeg', bbox_inches='tight')
    plt.show()

