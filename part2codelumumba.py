def get_outliers(data, x_col, cols):
    outliers = set()

    for col in cols:
        groups = data.groupby(x_col)[col]
        IQR   = groups.quantile(0.75) - groups.quantile(0.25)
        upper = groups.quantile(0.75) + 1.5 * IQR
        lower = groups.quantile(0.25) - 1.5 * IQR

        for idx, row in data.iterrows():
            val  = row[col]
            part = row[x_col]
            if val > upper[part] or val < lower[part]:
                outliers.add(idx)

    return data.loc[sorted(outliers)]

